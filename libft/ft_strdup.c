#include "libft.h"

char		*ft_strdup(const char *s1)
{
	char	*s2;
	int		i;

	i = 0;
	if (!(s2 = ft_strnew(ft_strlen(s1))))
		return (NULL);
	while (*s1)
	{
		s2[i] = *s1;
		s1 += 1;
		i++;
	}
	return (s2);
}
