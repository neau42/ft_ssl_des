#include "libft.h"

char				*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t			i;

	i = 0;
	while (*src && n)
	{
		dst[i] = *src;
		i++;
		src++;
		n--;
	}
	ft_bzero(&dst[i], n);
	return (dst);
}
