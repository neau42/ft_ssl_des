#include "ft_ssl.h"

static uint8_t g_pc1[] = {57, 49, 41, 33, 25, 17, 9, 1, 58, 50, 42, 34, 26, 18,
	10, 2, 59, 51, 43, 35, 27, 19, 11, 3, 60, 52, 44, 36, 63, 55, 47, 39, 31,
	23, 15, 7, 62, 54, 46, 38, 30, 22, 14, 6, 61, 53, 45, 37, 29, 21, 13, 5, 28,
	20, 12, 4};

static uint8_t	g_pc2[] = {14, 17, 11, 24, 1, 5, 3, 28, 15, 6, 21, 10, 23, 19,
	12, 4, 26, 8, 16, 7, 27, 20, 13, 2, 41, 52, 31, 37, 47, 55, 30, 40, 51, 45,
	33, 48, 44, 49, 39, 56, 34, 53, 46, 42, 50, 36, 29, 32};

void		des_gen_keytab(uint64_t key, uint64_t *k)
{
	int			i;
	uint64_t	tmp;
	uint32_t	c0;
	uint32_t	d0;

	tmp = permut_bits(64, 56, key, g_pc1);
	c0 = 0xFFFFFFF & (tmp >> 28);
	d0 = 0xFFFFFFF & tmp;
	i = 0;
	while (++i < 17)
	{
		c0 = l_rot28(c0, (i < 3 || i == 9 || i == 16) ? 1 : 2);
		d0 = l_rot28(d0, (i < 3 || i == 9 || i == 16) ? 1 : 2);
		k[i - 1] = permut_bits(56, 48, d0 + ((uint64_t)c0 << 28), g_pc2);
	}
	i = 0;
}
